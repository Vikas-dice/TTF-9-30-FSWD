/*

switch and case 

*/

let day="A";
switch(day){
    case 1:console.log(" its one");
    break;
    case 2:console.log("its two");
    break;
    case 'A':console.log("its an A");
    break;
    case "vikas":console.log("its vikas");
    break;
    case "Monday":console.log("its monday");
    break;
    case "Tuesday":console.log("its tuesday");
    break;
    case "Wednesday":console.log("its wednesday");
    break;
    case "Thursday":console.log("its thursday");
    break;
    case "friday":console.log("its friday");
    break;
    case "saturday":console.log("its saturday");
    break;
    case "sunday":console.log("its sunday");
    break;
    default:console.log("default case");

}
